var brick_index = 0;

var fs = require('fs');
var stream = fs.createWriteStream("bricks.vhd");
stream.once('open', function(fd) {
	for (var letter_index = 0; letter_index < 4; letter_index++) {
		for (var x_index = 0; x_index < 7; x_index++) {
			for (var y_index = 0; y_index < 25; y_index++) {
				if (letters[letter_index][y_index][x_index] === 1) {
					var brick_x = 60 + 466 * letter_index + 57 * x_index;
					var brick_y = 140 + 32 * y_index;
					stream.write("bricks_x(" + brick_index + ") <= " + brick_x + ";\n");
					stream.write("bricks_y(" + brick_index + ") <= " + brick_y + ";\n");
					brick_index++;
				}
			}
		}
	}
	stream.end();
});

